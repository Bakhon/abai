            <datetime
                v-model="filter['date']"
                :flow="['year', 'month', 'date']"
                :format="{ year: 'numeric', month: 'numeric', day: 'numeric'}"
                :phrases="{ok: trans('bd.select'), cancel: trans('bd.exit')}"
                auto
                input-class="form-control"
                type="date"
                value-zone="Asia/Almaty"
                zone="Asia/Almaty"
            >
            </datetime>
          </div>
        </template>
      </template>
    </div>
    <div class="bd-main-block__body">
      <BigDataTableForm
