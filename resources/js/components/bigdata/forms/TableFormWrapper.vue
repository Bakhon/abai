<script>
import Vue from "vue";
import moment from 'moment'
import {Datetime} from 'vue-datetime'
import 'vue-datetime/dist/vue-datetime.css'
import {bTreeView} from 'bootstrap-vue-treeview'
import {globalloadingMutations} from '@store/helpers'
import BigDataTableForm from './TableForm'
import BigdataFormField from './field'

Vue.use(Datetime)

export default {
  name: "BigDataTableFormWrapper",
  props: {
    params: {
      type: Object,
      required: true
    },
    id: {
      type: Number,
      required: false
    },
    type: {
      type: String,
      required: false
    },
  },
  components: {
    bTreeView,
    BigDataTableForm,
    BigdataFormField
  },
  data() {
    return {
      filter: null
    }
  },
  watch: {
    params() {
      this.init()
    },
    filter: {
      handler(val) {
        this.init()
      },
      deep: true
    }
  },
  mounted() {
    this.init()
  },
  methods: {
    ...globalloadingMutations([
      'SET_LOADING'
    ]),
    filterForm(item, isSelected) {
      if (isSelected) {
        if (item.data.type === 'org') return false
        this.id = item.data.id
        this.type = item.data.type
      }
    },
    initFilter() {
      if (this.filter) return

      if (!this.formParams.filter) {
        this.filter = {date: moment().toISOString()}
        return
      }

      let filter = {}
      this.formParams.filter.forEach(filterItem => {
        filter[filterItem.code] = filterItem.type === 'date' ? moment().toISOString() : null
      })

      this.filter = filter

    },
    init(formParams) {
      this.formParams = formParams
      this.initFilter()
    },
  },
};
</script>
